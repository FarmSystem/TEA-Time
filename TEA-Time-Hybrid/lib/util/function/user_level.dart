String getLevelAsset(int level) {
  switch (level) {
    case 1:
      return 'assets/images/level1.svg';
    case 2:
      return 'assets/images/level2.svg';
    case 3:
      return 'assets/images/level3.svg';
    case 4:
      return 'assets/images/level4.svg';
    case 5:
      return 'assets/images/level5.svg';
    case 6:
      return 'assets/images/level6.svg';
    case 7:
      return 'assets/images/level7.svg';
    default:
      return 'assets/images/level1.svg';
  }
}